        function selectUniversity(){
            var departIndex,univIndex;
            univIndex = $("#school").get(0).selectedIndex;  //获取选择的大学
            
            var univCount=0; //用于得到该校的院系数
            //计算选定学校的院系个数
            while(university[univIndex][univCount]!=null)
                univCount++;
            
            //改变第二个下拉框的选项数
            $("#depart").get(0).length=univCount;
            //改变第二个下拉框的内容
            for(departIndex = 0; departIndex < univCount; departIndex++)
            	$("#depart").get(0)[departIndex]= new Option(university[univIndex][departIndex]);
            //第二个下拉框获得焦点
            document.univ_depart.department.focus();
        }